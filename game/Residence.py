class Residence:
    def __init__(self, house, appartment, villa, mansion, castle):
        self.house = house
        self.appartment = appartment
        self.villa = villa
        self.mansion = mansion
        self.castle = castle

    def get_house(self):
        return self.house
    
    def get_appartment(self):
        return self.appartment
        
    def get_villa(self):
        return self.villa
    
    def get_mansion(self):
        return self.mansion
    
    def get_castle(self):
        return self.castle



        